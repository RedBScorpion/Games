function MoonLib:IsRunning()
	if gethui then
		return Moon.Parent == gethui()
	else
		return Moon.Parent == game:GetService("CoreGui")
	end

end

local function AddConnection(Signal, Function)
	if (not MoonLib:IsRunning()) then
		return
	end
	local SignalConnect = Signal:Connect(Function)
	table.insert(MoonLib.Connections, SignalConnect)
	return SignalConnect
end

task.spawn(function()
	while (MoonLib:IsRunning()) do
		wait()
	end

	for _, Connection in next, MoonLib.Connections do
		Connection:Disconnect()
	end
end)

local function Create(Name, Properties, Children)
	local Object = Instance.new(Name)
	for i, v in next, Properties or {} do
		Object[i] = v
	end
	for i, v in next, Children or {} do
		v.Parent = Object
	end
	return Object
end

local function CreateElement(ElementName, ElementFunction)
	MoonLib.Elements[ElementName] = function(...)
		return ElementFunction(...)
	end
end

local function MakeElement(ElementName, ...)
	local NewElement = MoonLib.Elements[ElementName](...)
	return NewElement
end

local function SetProps(Element, Props)
	table.foreach(Props, function(Property, Value)
		Element[Property] = Value
	end)
	return Element
end

local function SetChildren(Element, Children)
	table.foreach(Children, function(_, Child)
		Child.Parent = Element
	end)
	return Element
end

local function Round(Number, Factor)
	local Result = math.floor(Number/Factor + (math.sign(Number) * 0.5)) * Factor
	if Result < 0 then Result = Result + Factor end
	return Result
end

local function ReturnProperty(Object)
	if Object:IsA("Frame") or Object:IsA("TextButton") then
		return "BackgroundColor3"
	end 
	if Object:IsA("ScrollingFrame") then
		return "ScrollBarImageColor3"
	end 
	if Object:IsA("UIStroke") then
		return "Color"
	end 
	if Object:IsA("TextLabel") or Object:IsA("TextBox") then
		return "TextColor3"
	end   
	if Object:IsA("ImageLabel") or Object:IsA("ImageButton") then
		return "ImageColor3"
	end   
end

local function AddThemeObject(Object, Type)
	if not MoonLib.ThemeObjects[Type] then
		MoonLib.ThemeObjects[Type] = {}
	end    
	table.insert(MoonLib.ThemeObjects[Type], Object)
	Object[ReturnProperty(Object)] = MoonLib.Themes[MoonLib.SelectedTheme][Type]
	return Object
end    

local function SetTheme()
	for Name, Type in pairs(MoonLib.ThemeObjects) do
		for _, Object in pairs(Type) do
			Object[ReturnProperty(Object)] = MoonLib.Themes[MoonLib.SelectedTheme][Name]
		end    
	end    
end

local function PackColor(Color)
	return {R = Color.R * 255, G = Color.G * 255, B = Color.B * 255}
end    

local function UnpackColor(Color)
	return Color3.fromRGB(Color.R, Color.G, Color.B)
end

local function LoadCfg(Config)
	local Data = HttpService:JSONDecode(Config)
	table.foreach(Data, function(a,b)
		if MoonLib.Flags[a] then
			spawn(function() 
				if MoonLib.Flags[a].Type == "Colorpicker" then
					MoonLib.Flags[a]:Set(UnpackColor(b))
				else
					MoonLib.Flags[a]:Set(b)
				end    
			end)
		else
			warn("Moon Library Config Loader - Could not find ", a ,b)
		end
	end)
end

local function SaveCfg(Name)
	local Data = {}
	for i,v in pairs(MoonLib.Flags) do
		if v.Save then
			if v.Type == "Colorpicker" then
				Data[i] = PackColor(v.Value)
			else
				Data[i] = v.Value
			end
		end	
	end
	writefile(MoonLib.Folder .. "/" .. Name .. ".txt", tostring(HttpService:JSONEncode(Data)))
end

